/**
 * @file    ch01_ex05_button_exti_debounce.c
 * @brief   인터럽트 + 타임스탬프 디바운싱 — Ch02 이후 베이스 코드
 *
 * @board   NUCLEO-F411RE (STM32F411RE)
 * @핀연결
 *   - B1 (파란 버튼) = PC13, 액티브 로우 (누름 = GPIO_PIN_RESET)
 *   - LD2 (녹색 LED) = PA5, 액티브 하이
 *
 * @CubeMX_설정 (필수)
 *   1. PC13: GPIO_EXTI13, Falling Edge (하강 엣지 인터럽트)
 *      - Pull-up 설정 (버튼 뗀 상태 = HIGH)
 *   2. NVIC: EXTI15_10 IRQ → Enabled
 *      - Preemption Priority: 5~10 권장 (0은 긴급 인터럽트용으로 예약)
 *        HAL 기본값: SysTick = Priority 15(최저). EXTI를 0~14 중 어떤 값으로
 *        설정해도 SysTick보다 높으며, ISR 실행 중 SysTick은 선점 불가.
 *      - Sub Priority: 0
 *   3. PA5: GPIO_OUTPUT, Push-Pull, No Pull, Speed=Low
 *
 * @디바운싱_원리 (타임스탬프 방식)
 *   HAL_Delay 방식의 문제: ISR 내부에서 Delay 사용 불가 (블로킹)
 *
 *   타임스탬프 방식:
 *     인터럽트 발생 시각을 기록해두고,
 *     다음 인터럽트까지의 시간 간격이 임계값(50ms) 미만이면 채터링으로 무시한다.
 *
 *   타임라인 예시:
 *     t=0ms:   첫 번째 인터럽트 → 처리 O, 타임스탬프 = 0
 *     t=3ms:   채터링 인터럽트 → (3-0) < 50 → 무시
 *     t=8ms:   채터링 인터럽트 → (8-0) < 50 → 무시
 *     t=53ms:  두 번째 실제 누름 → (53-0) >= 50 → 처리 O, 타임스탬프 = 53
 *
 * @ISR_→_메인_통신_패턴
 *   ISR은 전역 플래그(g_button_flag)를 세팅하고,
 *   메인 루프가 플래그를 감지하여 처리 후 클리어한다.
 *   이 패턴은 ISR 처리 시간을 최소화하는 표준적인 방법이다.
 *
 * TODO: Ch02에서 LOG 시스템 추가
 */

#include "main.h"

/* =========================================================
 * 전역 변수
 * ========================================================= */

/**
 * @brief  ISR → 메인 루프 통신용 플래그
 *
 * volatile 키워드 설명:
 *   컴파일러는 최적화 과정에서 반복 참조되는 변수를 CPU 레지스터에 캐시한다.
 *   메인 루프가 g_button_flag를 레지스터에 캐시하면,
 *   ISR이 메모리의 값을 바꿔도 메인 루프는 캐시된 옛 값을 계속 읽는다.
 *   → 버튼을 눌러도 메인 루프가 영원히 반응하지 않는 버그 발생!
 *
 *   volatile은 컴파일러에게 "이 변수는 항상 메모리에서 직접 읽어라"고 지시한다.
 *   ISR과 메인 루프처럼 다른 실행 컨텍스트가 공유하는 변수에 반드시 필요하다.
 */
volatile uint8_t g_button_flag = 0;    /* 0: 이벤트 없음, 1: 버튼 눌림 감지 */

/**
 * @brief  마지막 버튼 누름의 타임스탬프 (HAL_GetTick 기준, ms 단위)
 *
 * static: 이 파일 내부에서만 접근 가능 (다른 파일에서 직접 접근 방지)
 * 초기값 0: 시스템 시작 시 0ms 기준
 */
static uint32_t s_last_press_tick = 0;

/* 디바운싱 임계값: 이 시간(ms) 이내의 재진입은 채터링으로 무시 */
#define DEBOUNCE_THRESHOLD_MS   50U

/* =========================================================
 * 인터럽트 콜백
 * ========================================================= */

/**
 * @brief  EXTI 인터럽트 콜백 — 디바운싱 포함
 *
 * @param  GPIO_Pin  인터럽트 발생 핀의 비트마스크
 *
 * @note   이 함수는 인터럽트 컨텍스트에서 실행된다.
 *           처리를 최소화하고 플래그만 세팅한 뒤 빠르게 반환한다.
 *
 * @note   HAL_GetTick()은 SysTick 인터럽트(1ms 주기)가 카운트를 증가시킨다.
 *           HAL 기본값: SysTick Priority = 15(최저). EXTI Priority는 0~14 중
 *           어떤 값이든 SysTick보다 높으므로, EXTI ISR이 실행되는 동안 SysTick은
 *           선점할 수 없다. 따라서 ISR 내 HAL_GetTick()은 최대 1ms 오차가
 *           발생할 수 있으나, 50ms 디바운싱 판정에는 문제가 되지 않는다.
 *           권장: EXTI Priority 5~10 범위 사용 (0은 긴급용으로 예약)
 *           (SysTick = Priority 15(최저, HAL 기본값), EXTI = Priority 5~10 권장)
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    /*
     * GPIO_PIN_13 체크
     * ⚠️ GPIO_PIN_13은 비트마스크(0x2000)이므로 반드시 == GPIO_PIN_13 으로 비교
     *    (== 13) 같은 리터럴 비교는 항상 거짓 → 버그
     */
    if (GPIO_Pin == GPIO_PIN_13)
    {
        /* 현재 시각 가져오기 (밀리초 단위, 시스템 시작 후 경과 시간) */
        uint32_t current_tick = HAL_GetTick();

        /*
         * 디바운싱 체크:
         *   마지막 처리 시각과 현재 시각의 차이가 임계값 미만이면
         *   채터링으로 판단하고 무시한다.
         *
         * uint32_t 오버플로우 처리:
         *   HAL_GetTick()은 약 49.7일 후에 0으로 오버플로우된다.
         *   (current_tick - s_last_press_tick) 연산은 unsigned 산술로 인해
         *   오버플로우가 발생해도 올바른 시간 차이를 계산한다.
         *   예: current_tick=5, s_last_press_tick=0xFFFFFFF0
         *       5 - 0xFFFFFFF0 = 0x15 = 21 (올바른 21ms 경과)
         */
        if ((current_tick - s_last_press_tick) >= DEBOUNCE_THRESHOLD_MS)
        {
            /* 50ms 이상 경과 → 실제 버튼 눌림으로 판정 */

            /* 플래그 세팅: 메인 루프에 이벤트 발생을 알린다 */
            g_button_flag = 1;

            /* 타임스탬프 갱신: 다음 인터럽트의 기준 시각으로 사용 */
            s_last_press_tick = current_tick;

            /* TODO: Ch02에서 LOG 시스템 추가 */
        }
        /* 50ms 미만 → 채터링으로 판단, 플래그 세팅 없이 그냥 반환 */
    }
}

/* =========================================================
 * 메인 함수
 * ========================================================= */

/**
 * @brief  메인 함수
 */
int main(void)
{
    /* --- 시스템 초기화 (CubeMX 자동 생성 코드) --- */
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();     /* PC13: EXTI Falling, PA5: 출력으로 설정됨 */

    /* --- 메인 루프 --- */
    while (1)
    {
        /*
         * 플래그 확인:
         *   g_button_flag는 volatile이므로 항상 메모리에서 직접 읽는다.
         *   ISR이 플래그를 1로 세팅하면 다음 루프 반복에서 감지된다.
         */
        if (g_button_flag != 0)
        {
            /*
             * 플래그 클리어 (처리 시작 전에 즉시 클리어)
             *
             * 순서 중요성:
             *   1. 플래그 클리어 → 2. 이벤트 처리  (올바른 순서)
             *   처리 중에 새 인터럽트가 와도 플래그가 다시 1로 세팅되므로
             *   다음 루프에서 처리된다.
             *
             *   만약 처리 → 클리어 순서라면:
             *   처리 도중 새 인터럽트 플래그가 처리 후 클리어되어 유실된다.
             */
            g_button_flag = 0;

            /* LED 토글 */
            HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);

            /* TODO: Ch02에서 LOG_D("버튼 눌림 감지") 추가 */
        }

        /*
         * 이 루프에 다른 작업을 자유롭게 추가할 수 있다.
         * 버튼 처리는 인터럽트가 담당하므로 폴링 오버헤드가 없다.
         *
         * Ch02 이후 여기에 추가될 기능 예시:
         *   - LOG 시스템 플러시
         *   - 상태 머신 업데이트
         *   - UART 통신 처리
         */
    }
}

